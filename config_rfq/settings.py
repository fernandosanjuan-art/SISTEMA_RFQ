# [PATCH - INTEGRACIÓN DE BUCKET R2] ------------------------------------
#
# En vez de pasar archivos por Base64 a través de Redis (el patrón que
# causaba el consumo alto de RAM), ahora el proceso web guarda el archivo
# directamente en un bucket R2, y el worker de Celery lo descarga de ahí
# usando solo una "key" (string corto), no el contenido completo.
#
# Esto también resuelve el problema de "no pudimos usar volumen compartido":
# ya no lo necesitas, porque el bucket ES el medio compartido entre el
# servicio web y el worker.
#
# Requisitos (agrega esto a requirements.txt):
#   django-storages
#   boto3
#
# Variables de entorno que debes configurar en Railway (web Y worker):
#   R2_ACCESS_KEY_ID
#   R2_SECRET_ACCESS_KEY
#   R2_BUCKET_NAME
#   R2_ENDPOINT_URL      -> https://<account_id>.r2.cloudflarestorage.com
#
# Si estas variables NO están definidas (por ejemplo en tu máquina local),
# el sistema sigue usando el FileSystemStorage de siempre, sin romper tu
# entorno de desarrollo.

USE_R2_STORAGE = bool(os.getenv('R2_BUCKET_NAME'))

if USE_R2_STORAGE:
    INSTALLED_APPS += ['storages']

    AWS_ACCESS_KEY_ID = os.getenv('R2_ACCESS_KEY_ID')
    AWS_SECRET_ACCESS_KEY = os.getenv('R2_SECRET_ACCESS_KEY')
    AWS_STORAGE_BUCKET_NAME = os.getenv('R2_BUCKET_NAME')
    AWS_S3_ENDPOINT_URL = os.getenv('R2_ENDPOINT_URL')
    AWS_S3_ADDRESSING_STYLE = 'virtual'
    AWS_S3_SIGNATURE_VERSION = 's3v4'
    AWS_S3_REGION_NAME = 'auto'  # R2 usa 'auto' como región
    AWS_DEFAULT_ACL = None
    AWS_QUERYSTRING_AUTH = True  # URLs firmadas con expiración, bucket privado
    AWS_S3_FILE_OVERWRITE = True

    STORAGES['default']['BACKEND'] = 'storages.backends.s3.S3Storage'
