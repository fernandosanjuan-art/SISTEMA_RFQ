# [PATCH - IMPORTANTE] Antes: CELERY_WORKER_MAX_TASKS_PER_CHILD = 1
#
# Con valor 1, Celery MATA el proceso worker después de CADA archivo procesado.
# El problema: PaddleOCR y SentenceTransformer('all-MiniLM-L6-v2') son modelos
# de cientos de MB que se cargan desde cero cada vez que arranca un proceso
# nuevo (ver ocr_service.py y embedding_matcher.py). Esto significa que estás
# pagando el costo de "cargar el modelo pesado en RAM" en CADA archivo, en vez
# de una sola vez por proceso.
#
# Subimos esto a 25: el proceso procesa 25 archivos, reutilizando los modelos
# ya cargados en memoria (ahorro grande de RAM y de tiempo), y luego se
# recicla para evitar fugas de memoria acumuladas de librerías C externas
# (PaddleOCR / opencv / cadquery), que sí pueden tener leaks graduales.
CELERY_WORKER_MAX_TASKS_PER_CHILD = 25

# [PATCH - IMPORTANTE] Antes: CELERY_WORKER_MAX_MEMORY_PER_CHILD = 250000 (250MB)
#
# PaddleOCR + SentenceTransformer + cadquery/trimesh cargados juntos en el
# mismo proceso muy probablemente superan 250MB solo en modelos, sin contar
# el archivo que se está procesando. Con este límite tan bajo, Railway puede
# estar matando y reiniciando el worker constantemente, lo que a su vez
# provoca que los modelos se recarguen una y otra vez (ciclo que CONSUME
# más RAM acumulada en el tiempo, no menos).
#
# Subimos el límite a 900MB (ajusta según lo que veas en Railway con
# `celery -A config_rfq worker --loglevel=info` y monitoreo real).
CELERY_WORKER_MAX_MEMORY_PER_CHILD = 900000  # 900 MB



